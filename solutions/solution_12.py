# Prompt the user for an integer
n = int(input("Enter an integer: "))

# Loop through the range of the integer
for i in range(1, n + 1):
    # Print the penguin
    print(f"   _~_        ", end="")
print()

for i in range(1, n + 1):
    # Print the penguin
    print(f"  (o o)       ", end="")
print()

for i in range(1, n + 1):
    # Print the penguin
    print(f" /  V  \\      ", end="")
print()

for i in range(1, n + 1):
    # Print the penguin
    print(f"/(  {i}  )\\     ", end="")
print()

for i in range(1, n + 1):
    # Print the penguin
    print(f"  ^^ ^^       ", end="")
print()
